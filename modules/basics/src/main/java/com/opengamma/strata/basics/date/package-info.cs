/// <summary>
/// Tools for working with dates.
/// <para>
/// This package contains data objects and tools for manipulating dates.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.DayCount DayCount"/> provides the standard
/// mechanism used to convert dates to fractions of a year.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.HolidayCalendar HolidayCalendar"/> provides
/// data on whether a date is a holiday or business day.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.BusinessDayConvention BusinessDayConvention"/>
/// provides standard rules for converting a holiday to the nearest business day,
/// such as 'Following', 'ModifiedFollowing' and 'Preceding'.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.PeriodAdditionConvention PeriodAdditionConvention"/>
/// provides rules for adding months, such as 'LastDay' and 'LastBusinessDay'.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.Tenor Tenor"/> is used to represent
/// the length of time that a financial instrument takes to reach maturity.
/// </para>
/// <para>
/// <seealso cref="com.opengamma.strata.basics.date.AdjustableDate AdjustableDate"/>,
/// <seealso cref="com.opengamma.strata.basics.date.BusinessDayAdjustment BusinessDayAdjustment"/>,
/// <seealso cref="com.opengamma.strata.basics.date.DaysAdjustment DaysAdjustment"/>,
/// <seealso cref="com.opengamma.strata.basics.date.DaysAdjustment PeriodAdjustment"/> and
/// <seealso cref="com.opengamma.strata.basics.date.DaysAdjustment TenorAdjustment"/> provide
/// entity objects to represent different kinds of adjustment.
/// </para>
/// </summary>
